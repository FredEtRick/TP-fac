#include"stdlib.h"
#include"stdio.h"

int main(int argc, char** argv){ 

	int i=1; // un iterateur
	int rep=0; // notre reponse

	
	while(i){ // Une erreur de debutant !
		printf("Je suis un mauvais programmeur, je devrais me reconvertir dans la biologie\n"); // l'affichage de test d'un developpeur visiblement inexperimente
		rep++;
		if(rep==42){
			i=0;
		}
	}

	printf("La reponse est %d\n", rep); // un affichage plutot utile, finalement

	return 0;
}
